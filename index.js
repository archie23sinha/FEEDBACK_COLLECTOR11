// shared/schema.ts
import { pgTable, text, serial, timestamp } from "drizzle-orm/pg-core";
import { createInsertSchema } from "drizzle-zod";
import { z } from "zod";
var feedbacks = pgTable("feedbacks", {
  id: serial("id").primaryKey(),
  name: text("name").notNull(),
  email: text("email").notNull(),
  message: text("message").notNull(),
  createdAt: timestamp("created_at").defaultNow().notNull()
});
var insertFeedbackSchema = createInsertSchema(feedbacks).pick({
  name: true,
  email: true,
  message: true
}).extend({
  email: z.string().email("Please enter a valid email address"),
  name: z.string().min(2, "Name must be at least 2 characters"),
  message: z.string().min(5, "Message must be at least 5 characters")
});

// server/routes.ts
import { ZodError } from "zod";
import { fromZodError } from "zod-validation-error";
async function registerRoutes(app2) {
  app2.get("/api/feedbacks", async (req, res) => {
    try {
      const feedbacks2 = await storage.getFeedbacks();
      return res.json(feedbacks2);
    } catch (error) {
      console.error("Error fetching feedbacks:", error);
      return res.status(500).json({
        message: "Failed to fetch feedback entries"
      });
    }
  });
  app2.post("/api/submit-feedback", async (req, res) => {
    try {
      const validatedData = insertFeedbackSchema.parse(req.body);
      const newFeedback = await storage.createFeedback(validatedData);
      return res.status(201).json(newFeedback);
    } catch (error) {
      if (error instanceof ZodError) {
        const validationError = fromZodError(error);
        return res.status(400).json({
          message: validationError.message
        });
      }
      console.error("Error submitting feedback:", error);
      return res.status(500).json({
        message: "Failed to submit feedback"
      });
    }
  });
  const httpServer = createServer(app2);
  return httpServer;
}

